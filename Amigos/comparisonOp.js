//< <= >= == !=

//Equality Operator
console.log(10 == 10)

//Inequality Operator
console.log(10 != 10)

//Strict Equality Operator
console.log(10 === 10)

//Strict Inequality Operator
console.log(10 !== 10)

//Less than
console.log(10 < 10)

//Greater than
console.log(10 > 10)

//Greater than or equal to
console.log(10 >=10)

//Less than or equal to
console.log(10 <= 10)